"""
Funciones varias.

Por ejemplo para duplicar un elemento, borrar otro, etc.
"""


def duplicar(x):
    """
    Duplica un elemento.

    Args:
        x (float): Elemento.

    Returns:
        List[float]: Elemento duplicado: ``[x, x]``.
    """
    return [x, x]


def intercambiar(x, y):
    """
    Intercambia dos números.

    Args:
        x (float): Un número.
        y (float): Otro número.

    Returns:
        List[float]: Elementos intercambiados: ``[x, y]``.
    """
    return [x, y]


